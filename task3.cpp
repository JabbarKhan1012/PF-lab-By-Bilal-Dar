#include <iostream>
using namespace std;
int main()
{
    int num=10;
    num+=5;
    cout<<"the number is:"<<num<<endl;
    num-=2;
    cout<<"the number is"<<num<<endl;
    num*=3;
    cout<<"the number is"<<num<<endl;
    num/=4;
    cout<<"the number is"<<num<<endl;
    num%=2;
    cout<<"the number is"<<num<<endl;
    cout<<"Final value of number is :"<<num<<endl;

}